public class variables {
    public static void main(String[] args) {
        int num1 = 5 ;
        System.out.println("The num1 is = " + num1);
        num1 = 10 ; 
        System.out.println("Now the num1 is = " + num1);
        num1 = 1000 ; 
        System.out.println("Now the num1 is assigned to = " + num1);

        System.out.println("Integer Value Range is from "+Integer.MIN_VALUE+" to "+Integer.MAX_VALUE);

    }
}
